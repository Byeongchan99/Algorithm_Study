#include <set>
using namespace std;

int GCD(int a, int b) {
	if (a % b == 0)
		return b;
	return GCD(b, a % b);
}


int main() {
	int N, first, n;
	scanf("%d", &N);

	scanf("%d", &first);
	for (int i = 0; i < N - 1; ++i) {
		scanf("%d", &n);
		int gcd = GCD(first, n);
		printf("%d/%d\n", first / gcd, n / gcd);
	}
}
